package ControlFlow;

public class ComparisonOperators {
    public static  void doOperation() {
        int x = 20;
        int y = 10;

        System.out.println(x < y); // false
        System.out.println(x <= y); // false
        System.out.println(x > y); // true
        System.out.println(x >= y); // true
        System.out.println(x == y); // false
        System.out.println(x != y); // true
    }
}
